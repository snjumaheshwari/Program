class Simple{
	
	public static void main(String[] ar)
	{
		int a=10;
		float f=a;
		
		System.out.println(a);
		System.out.println(f);
		
	}
	
	
}

class Simple2{
	
	public static void main(String[] ar)
	{
		
		float f=10;
		int a=(int)f;
		
		
		System.out.println(a);
		System.out.println(f);
		
	}
	
	
}

